import * as assert from "node:assert";

describe("InventoryComponent", () => {
    it("can get items", () => {
        assert.equal(2, 2);
    });

    it("adding inventory item to existing entry", () => {
        assert.equal(2, 2);
    });

    it("adding inventory item to new entry", () => {
        assert.equal(2, 2);
    });

    it("remove full amount from inventory item", () => {
        assert.equal(2, 2);
    });

    it("remove full partial amount from inventory item", () => {
        assert.equal(2, 2);
    });

    it("attempt to remove more than available amount fails", () => {
        assert.equal(2, 2);
    });
});
